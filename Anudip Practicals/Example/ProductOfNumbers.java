import java.util.*;
 
public class ProductOfNumbers {
  public static void main(String[] args) 
{
  Scanner in = new Scanner(System.in);
  System.out.print("First number: ");
  int num1 = in.nextInt();
  System.out.print("Second number: ");
  int num2 = in.nextInt();
   
  System.out.println(num1 + " * " + num2 + " = " + num1 * num2);
 }
 
}